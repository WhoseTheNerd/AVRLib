#define BYTE_TO_BINARY_PATTERN "%c%c%c%c%c%c%c%c"
#define BYTE_TO_BINARY(byte)  \
  (byte & 0b10000000 ? '1' : '0'), \
  (byte & 0b01000000 ? '1' : '0'), \
  (byte & 0b00100000 ? '1' : '0'), \
  (byte & 0b00010000 ? '1' : '0'), \
  (byte & 0b00001000 ? '1' : '0'), \
  (byte & 0b00000100 ? '1' : '0'), \
  (byte & 0b00000010 ? '1' : '0'), \
  (byte & 0b00000001 ? '1' : '0') 

